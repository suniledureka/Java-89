
public class CharacterTypes {

	public static void main(String[] args) {
		char c = 'A';
		System.out.println(c);
		
		c = 65; 
		System.out.println(c);
		
		c = '\u0041';
		System.out.println(c);
		
		int n = 'a';
		System.out.println(n);
		
		c = '\u0905';
		System.out.println(c);
		
		c = '\u4eca';
		System.out.println(c);
	}

}
