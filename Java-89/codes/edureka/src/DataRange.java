
public class DataRange {

	public static void main(String[] args) {
		System.out.println("min value for byte type = " + Byte.MIN_VALUE);
		System.out.println("max value for byte type = " + Byte.MAX_VALUE);
		System.out.println();
		
		System.out.println("min value for short type = " + Short.MIN_VALUE);
		System.out.println("max value for short type = " + Short.MAX_VALUE);
		System.out.println();
		
		System.out.println("min value for int type = " + Integer.MIN_VALUE);
		System.out.println("max value for int type = " + Integer.MAX_VALUE);
		System.out.println();	
		
		System.out.println("min value for long type = " + Long.MIN_VALUE);
		System.out.println("max value for long type = " + Long.MAX_VALUE);
		
	}

}
